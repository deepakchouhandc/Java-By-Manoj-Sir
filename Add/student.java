package Add;

import java.time.Period;

public class student {
   
    private int rollno;
    private String name;
    private float per;
    private boolean govStudent;

    public int getRollno() {
        return rollno;
    }

    public void setRollno(int rollno) {
        this.rollno = rollno;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getPer() {
        return per;
    }

    public void setPer(float per) {
        this.per = per;
    }

    public boolean isGovStudent() {
        return govStudent;
    }

    public void setGovStudent(boolean govStudent) {
        this.govStudent = govStudent;
    }
    
     
}
